import UIKit

extension UIColor {
    
    open class var skyBlueColor: UIColor {
        return UIColor(red: 159/255, green: 201/255, blue: 244/255, alpha: 1)
    }
    
    
    open class var lightGreen: UIColor {
        return UIColor(red: 88/255, green: 148/255, blue: 87/255, alpha: 1)
    }
    
    open class var darkGreen: UIColor {
        return UIColor(red: 120/255, green: 195/255, blue: 118/255, alpha: 1)
    }
    
    open class var dragonGreen: UIColor {
        return UIColor(red: 33/255, green: 143/255, blue: 106/255, alpha: 1)
    }
}
